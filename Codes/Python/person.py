class Person():
    """docstring for Person."""
    def __init__(self, name, age):
        self.name = name
        self.age = age

    def eat(food):
        return "eat "+food

    def work(company):
        return "work for "+company

    def getName(self):
        return self.name

    def setName(name):
        self.name = name

    def getAge(self):
        return self.age

    def setAge(age):
        self.age = age
